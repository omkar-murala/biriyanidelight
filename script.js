const menuItemsContainer = document.getElementById("menu-items");
const categories = document.querySelectorAll(".category-card");

function loadCategory(category) {
  menuItemsContainer.innerHTML = `<div class="loading">Loading ${category}...</div>`;

  setTimeout(() => {
    menuItemsContainer.innerHTML = "";
    const items = menuData[category] || [];

    items.forEach(item => {
      const card = document.createElement("div");
      card.className = "menu-card";
      card.innerHTML = `
        <img src="${item.img}" alt="${item.name}" loading="lazy"/>
        <div class="card-content">
          <h3>${item.name}</h3>
          <p>${item.desc}</p>
          ${
            item.half && item.full
              ? `<div class="price-split">
                  <div class="half">
                    <span>Half</span>
                    <span class="price">${item.half}</span>
                  </div>
                  <div class="full">
                    <span>Full</span>
                    <span class="price">${item.full}</span>
                  </div>
                </div>`
              : item.full
              ? `<div class="price-split">
                  <div class="full">
                    <span>Full</span>
                    <span class="price">${item.full}</span>
                  </div>
                </div>`
              : item.price
              ? `<div class="price-split">
                  <div class="full">
                    <span>Price</span>
                    <span class="price">${item.price}</span>
                  </div>
                </div>`
              : `<p class="price">Price not available</p>`
          }
        </div>
      `;
      card.querySelector("img").onerror = () => {
        card.querySelector("img").src = "https://via.placeholder.com/300x200?text=Image+Unavailable";
      };

      menuItemsContainer.appendChild(card);
    });

    menuItemsContainer.scrollTop = 0;
  }, 100);
}

categories.forEach(btn => {
  btn.setAttribute("tabindex", "0");

  btn.addEventListener("click", () => {
    categories.forEach(b => b.classList.remove("active"));
    btn.classList.add("active");
    const category = btn.dataset.category;
    loadCategory(category);
  });

  btn.addEventListener("keypress", (e) => {
    if (e.key === "Enter") {
      btn.click();
    }
  });
});

loadCategory("BIRYANI");
document.querySelector('[data-category="BIRYANI"]').classList.add("active");
